package One;

public class Main {

    public static void main(String[] args) {
        int a = 500;
        for (;a<=650;a+=10){
            System.out.println(a);
        }
        int q = 500;
        while (q<=650){
            System.out.println(q);
            q +=10;
        }
        int w = 500;
        do {
            System.out.println(w);
            w+=10;
        }while (w <=650);
    }
}